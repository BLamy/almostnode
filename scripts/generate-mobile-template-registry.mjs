import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, relative, resolve, posix } from "node:path";

const repoRoot = resolve(new URL(".", import.meta.url).pathname, "..");
const templatesRoot = resolve(repoRoot, "apps/web-ide/src/templates/content");
const outputPath = resolve(repoRoot, "apps/mobile-ide/src/generated/mobile-template-registry.ts");

function walkFiles(directoryPath) {
  const files = [];
  for (const entry of readdirSync(directoryPath, { withFileTypes: true })) {
    const fullPath = join(directoryPath, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkFiles(fullPath));
      continue;
    }
    files.push(fullPath);
  }
  return files;
}

function collectDirectories(filePaths) {
  const directories = new Set();
  for (const filePath of filePaths) {
    let currentDirectory = dirname(filePath);
    while (currentDirectory && currentDirectory !== ".") {
      directories.add(currentDirectory);
      currentDirectory = dirname(currentDirectory);
    }
  }
  return [...directories].sort();
}

function encodeTemplateFiles(rootDirectory) {
  const files = {};
  for (const absolutePath of walkFiles(rootDirectory)) {
    const relativePath = relative(rootDirectory, absolutePath).split("\\").join("/");
    files[relativePath] = readFileSync(absolutePath).toString("base64");
  }
  return files;
}

const sharedFiles = (() => {
  const sharedDirectory = join(templatesRoot, "_shared");
  try {
    return encodeTemplateFiles(sharedDirectory);
  } catch {
    return {};
  }
})();

const templates = {};
for (const entry of readdirSync(templatesRoot, { withFileTypes: true })) {
  if (!entry.isDirectory() || entry.name === "_shared") {
    continue;
  }

  const templateDirectory = join(templatesRoot, entry.name);
  const metadata = JSON.parse(readFileSync(join(templateDirectory, "template.json"), "utf8"));
  const files = { ...sharedFiles };
  for (const absolutePath of walkFiles(templateDirectory)) {
    const relativePath = relative(templateDirectory, absolutePath).split("\\").join("/");
    if (relativePath === "template.json") {
      continue;
    }
    files[relativePath] = readFileSync(absolutePath).toString("base64");
  }

  templates[entry.name] = {
    id: entry.name,
    defaultFile: posix.join("/project", metadata.defaultFile),
    runCommand: metadata.runCommand,
    directories: collectDirectories(Object.keys(files)).map((directory) => posix.join("/project", directory)),
    files,
  };
}

const banner = [
  "// This file is generated by scripts/generate-mobile-template-registry.mjs",
  "// Do not edit it by hand.",
  "",
  "export type TemplateId = \"vite\" | \"nextjs\" | \"tanstack\";",
  "",
  "export interface MobileTemplateDefinition {",
  "  id: TemplateId;",
  "  defaultFile: string;",
  "  runCommand: string;",
  "  directories: string[];",
  "  files: Record<string, string>;",
  "}",
  "",
  `export const MOBILE_TEMPLATE_REGISTRY = ${JSON.stringify(templates, null, 2)} as const satisfies Record<TemplateId, MobileTemplateDefinition>;`,
  "",
].join("\n");

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, banner, "utf8");
